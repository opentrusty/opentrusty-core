package id

import "github.com/google/uuid"

// NewUUIDv7 generates a new UUIDv7 in canonical string format.
//
// Purpose: Primary identifier generation for all system entities.
// Domain: Platform
// Security: Provides time-ordered, collision-resistant identifiers suitable for database keys.
// Audited: No
// Errors: None
func NewUUIDv7() string {
	id, _ := uuid.NewV7()
	return id.String()
}
